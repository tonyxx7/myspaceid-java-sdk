package com.myspace.myspaceid;

/**
 * Class that encapsulates the type of application.
 * ON_SITE means an app that gets installed in MySpace and is run within MySpace.  
 * OFF_SITE means an app that runs completely outside of MySpace.
 */
public class ApplicationType
{
	public static final int ON_SITE = 0;
	public static final int OFF_SITE = 1;
}